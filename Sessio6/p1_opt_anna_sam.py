import numpy as np

def simplex(c, A, b, minimize=False, verbose=False):
    """
    Algoritmo simplex (fase II) con regla de Bland y criterio de desempate ajustado.
    
    Parámetros:
      c: vector de coeficientes (longitud n)
      A: matriz de restricciones (m x n) en forma estándar (Ax = b)
      b: vector de lados derechos (longitud m), se asume b >= 0
      minimize: True si el problema es de minimización (se convierte a maximización)
      verbose: si True, imprime información en cada iteración.
      
    Retorna:
      optimal_value: valor óptimo (según el problema original)
      x: vector de solución óptima (longitud n)
      basis: lista con los índices de las variables básicas
    """
    tol = 1e-12  # tolerancia más estricta para detectar coeficientes cero
    c = np.array(c, dtype=float)
    A = np.array(A, dtype=float)
    b = np.array(b, dtype=float)
    m, n = A.shape

    # Convertir problema de minimización en maximización, si es necesario.
    if minimize:
        c = -c

    # Construir el tableau (m+1 filas, n+1 columnas)
    tableau = np.zeros((m+1, n+1))
    tableau[:m, :n] = A
    tableau[:m, -1] = b
    # Fila objetivo (se usa -c para maximización)
    tableau[m, :n] = -c

    # Suposición: la SBF inicial es trivial; se asume que las últimas m variables son básicas.
    basis = list(range(n - m, n))

    if verbose:
        print("Tableau inicial:")
        print(tableau)
        print("Básicos iniciales:", basis)

    while True:
        # Paso 1: Seleccionar variable de entrada usando Bland (mínimo índice con coeficiente negativo)
        entering_candidates = [j for j in range(n) if tableau[m, j] < -tol]
        if not entering_candidates:
            break
        j = min(entering_candidates)
        
        # Paso 2: Seleccionar la fila de salida (variable saliente)
        ratios = []
        for i in range(m):
            if tableau[i, j] > tol:
                ratio = tableau[i, -1] / tableau[i, j]
                ratios.append((ratio, i))
        if not ratios:
            raise Exception("El problema es no acotado.")
        # Modificación: en caso de empate, se escoge la fila con el menor índice (desempate exacto)
        pivot_row = min(ratios, key=lambda x: (round(x[0], 12), x[1]))[1]

        if verbose:
            print(f"Pivot: variable de entrada j = {j}, fila de salida = {pivot_row}")

        # Paso 3: Operación de pivote
        pivot = tableau[pivot_row, j]
        tableau[pivot_row, :] /= pivot
        for i in range(m+1):
            if i != pivot_row:
                tableau[i, :] -= tableau[i, j] * tableau[pivot_row, :]
        basis[pivot_row] = j

        if verbose:
            print("Tableau actualizado:")
            print(tableau)
            print("Básicos:", basis)

    # Extraer la solución
    x = np.zeros(n)
    for i in range(m):
        x[basis[i]] = tableau[i, -1]
    optimal_value = tableau[m, -1]
    if minimize:
        optimal_value = -optimal_value

    return optimal_value, x, basis

def resolver_problema(c, A_rows, b, problem_id, minimize=True, verbose=False):
    print(f"Resolviendo problema PL {problem_id} (minimización)...")
    optimal_value, x, basis = simplex(c, A_rows, b, minimize=minimize, verbose=verbose)
    print(f"Valor óptimo (z*): {optimal_value:.4f}")
    print("Solución óptima (x*):")
    print(x)
    print("Índices de variables básicas:", basis)
    print("-" * 50)
    return optimal_value, x, basis

